#Scalar integers

# $VARIABLE_NAME = integer

$integer = 5;

#standard + - * /

#add
$add5= $integer + 5;
#prints 10
print $add5;

print "\n";

#also can add using += or ++
$add5 +=5;
#prints 15
print $add5;

print "\n";

$add5 ++;
#prints 16
print $add5;

print "\n";

#subtract
$minus5= $integer -5;
#prints 0
print $minus5;

print "\n";

#also can subtract using -=
$minus5 -= 5;
#prints -5
print $minus5;

print "\n";

#multiply
$multiply5 = $integer * 5;
#prints 25
print $multiply5;

print "\n";

#also can multiply using *=
$multiply5 *= 5;
#prints 125
print $multiply5;

print "\n";

#divide
$divide5 = $integer / 5;
#prints 1
print $divide5;

print "\n";

#also can divide using /=
$divide5 /= 5;
#prints 0.2
print $divide5;

print "\n";
